package JAVA;

public class TestaCaracteres {

    public static void main (String[] args) {

        char letra = 'a';
        System.out.println(letra);

        char valor = 64;
        System.out.println(valor);

        valor = (char) (valor + 1);
        System.out.println(valor);

        String palavra = "Olavo de Caralho";
        System.out.println(palavra);

        palavra = palavra + 2001;
        System.out.println(palavra);

    }

}